package kosmo_pis;

public class Print {

	public static void main(String[] args) {
		System.out.println("화면출력");
		
		
		System.out.println("화면"+"출력"); // "+" 문자열일때 연결 연산자
		System.out.println(3.8); // 정수, 실수를 출력할 때는 ""를 넣지 않음
		System.out.println(3+5); // 정수, 실수 일때의 +는 더하기의 기능
		System.out.println("3" + "5");
		System.out.println("3+5");
		System.out.println("화면: " + 3);
		System.out.println("화면: " +3 +5);
		System.out.println(3 + 5 + "화면");
		System.out.println("화면: "+(3+5));
		System.out.println("화면");
	}

}
